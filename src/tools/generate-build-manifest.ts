// ─── Main Function ──────────────────────────────────────────────────────────

export function generateBuildManifest(input: BuildManifestInput): BuildManifestOutput {
  const {
    projectName,
    framework,
    pages,
    uiverseComponents = [],
    builtInComponents = [],
    designSummary,
    auditScores,
  } = input;

  const generatedAt = input.generatedAt || new Date().toISOString().split('T')[0];

  // Count totals
  const allUsedComponents = new Set<string>();
  pages.forEach(p => p.componentsUsed.forEach(c => allUsedComponents.add(c)));
  const totalComponents = allUsedComponents.size + uiverseComponents.length;

  // Build the markdown document
  let markdown = '';

  // Header
  markdown += generateProjectHeader(
    projectName,
    framework,
    generatedAt,
    pages.length,
    totalComponents
  );

  // Design summary (if available)
  if (designSummary) {
    markdown += generateDesignSection(designSummary);
  }

  // Audit scores (if available)
  if (auditScores && (
    auditScores.seoScore !== undefined ||
    auditScores.consistencyScore !== undefined ||
    auditScores.reviewScore !== undefined
  )) {
    markdown += generateAuditSection(auditScores);
  }

  // Per-page breakdown
  markdown += `## Pages\n\n`;

  pages.forEach((page, i) => {
    markdown += generatePageSection(page, builtInComponents, uiverseComponents, i);
  });

  // Component reference
  if (builtInComponents.length > 0 || uiverseComponents.length > 0) {
    markdown += generateComponentSummary(builtInComponents, uiverseComponents);
  }

  // Footer
  markdown += generateFooter();

  // Build summary text
  const summary = [
    `Build manifest generated for "${projectName}".`,
    `${pages.length} page(s), ${totalComponents} component(s) documented.`,
    builtInComponents.length > 0 ? `${builtInComponents.length} from built-in library.` : '',
    uiverseComponents.length > 0 ? `${uiverseComponents.length} from UIverse GitHub.` : '',
    `Framework: ${framework}.`,
  ].filter(Boolean).join(' ');

  return {
    markdown,
    summary,
    totalComponents,
    totalPages: pages.length,
    uiverseCount: uiverseComponents.length,
    builtInCount: builtInComponents.length,
  };
}
